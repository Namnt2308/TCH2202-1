class User {
    constructor (id, department_id, username, password, fullName, dob, role, phone, status) {
        this.id = id;
        this.department_id = department_id;
        this.username = username;
        this.password = password;
        this.fullName = this.fullName;
        this.dob = dob;
        this.role = role;
        this.phone = phone;
        this.status = status;
    }
}